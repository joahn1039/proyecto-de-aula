<p>La computadora es una máquina digital que ejecuta comandos para convertirlos 
    en datos convenientes y útiles que posteriormente se envían a las unidades de salida.
    Un computador está formado físicamente por numerosos circuitos integrados y muchos componentes de apoyo, extensión y accesorios, 
    que en conjunto pueden ejecutar tareas diversas con suma rapidez y bajo el control de un programa (software).</p>

