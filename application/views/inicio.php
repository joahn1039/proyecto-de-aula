<p>que es?
    La tecnología es la aplicación de la ciencia a la 
    resolución de problemas concretos. Constituye un conjunto de conocimientos científicamente ordenados, que permiten diseñar y crear bienes o 
    servicios que facilitan la adaptación al medio ambiente, así como la satisfacción de las necesidades individuales esenciales y las aspiraciones de la humanidad.
     Aunque hay muchas tecnologías muy diferentes entre sí, es frecuente usar el término tecnología en singular para referirse al conjunto de todas, 
     o también a una de ellas en particular. La palabra tecnología también se puede referir a la disciplina teórica que estudia los saberes comunes a todas las tecnologías, 
     y en algunos contextos, a la educación tecnológica, la disciplina escolar abocada a la familiarización con las tecnologías más importantes.

